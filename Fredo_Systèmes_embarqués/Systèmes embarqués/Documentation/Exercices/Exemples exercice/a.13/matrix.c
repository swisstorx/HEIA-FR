#include <stdio.h>

static int matrix[2][3][4] = {
	{{111,112,113,114}, {121,122,123,124}, {131,132,133,134}}, 
	{{211,212,213,214}, {221,222,223,224}, {231,232,233,234}}
};


int main()
{
	int* t = &matrix[0][0][0];
	for (int i=0;i<2*3*4;i++) {
		printf ("%d  ", *t++);
		if ((i%4) == 3) printf ("\n");
	}
	return 0;
}
